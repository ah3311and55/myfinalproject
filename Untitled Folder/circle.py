import turtle
turtle.shape("turtle")
radius=55
turtle.pensize(7)
turtle.circle(radius,150)
turtle.penup()
turtle.pencolor("red")
turtle.fd(120)
turtle.pendown()
turtle.circle(radius)
turtle.penup()
turtle.backward(240)
turtle.pendown()
turtle.pencolor("blue")
turtle.circle(radius)
turtle.right(90)
turtle.penup()
turtle.fd(radius)
turtle.left(90)
turtle.fd(60)
turtle.pendown()
turtle.pencolor("yellow")
turtle.circle(radius)
turtle.penup()
turtle.fd(120)
turtle.pencolor("green")
turtle.pendown()
turtle.circle(radius)

turtle.done()
